package week6_Homework;

public class Task9_UpperCaseToLower {
/*
        9. Write a program to convert the upper case to lower case.

 */

    //Main method
    public static void main(String[] args) {
        String txt = "MY NAME IS ANISH VEKARIA."; //declare local variable
        System.out.println("Uppercase: " + txt); //print statement
        System.out.println("Lowercase: " + txt.toLowerCase()); //print to lowercase
    }
}
